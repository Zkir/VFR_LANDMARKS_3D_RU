def achitectureStylesRus(style):
    if style!="" and style[0]=="~":
        style=style[1:]
        prefix="~"
    else:    
        prefix=""
    
    if ";" in style:
        #"Эклектика" -- это когда стилей больше одного. Учитесь, сынки!
        style = 'eclectic'  
        
    return prefix+achitecture_styles_rus_names.get(style,style)